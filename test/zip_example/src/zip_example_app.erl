-module(zip_example_app).

-behaviour(application).

%% Application callbacks
-export([start/2, stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
    lager:start(),
    %% ideally this will be dynamic
    ezk:register_app(zip_example, #{name => <<"zip-example">>,
                                    ipv4 => <<"127.0.0.0">>,
                                    port => <<"8080">>}),
    zip_example_sup:start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

